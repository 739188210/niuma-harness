// 为各 agent 写入 native rules 入口；规则正文仍以 docs/rules 为唯一来源。
const fs = require('fs');
const path = require('path');

const { getAvailableRuleDirs, getRuleAdapterTargetsForAgent } = require('../rules');
const {
  removeEmptyDirsUntil,
  removeFile,
  safeResolveInside,
  writeFile,
} = require('../fs-safe');

const MANAGED_RULES_BEGIN = '<!-- niuma-harness:rules begin -->';
const MANAGED_RULES_END = '<!-- niuma-harness:rules end -->';

function writeRuleAdapterFiles(context) {
  const targets = getRuleAdapterTargetsForAgent(context.options.agent);
  const targetKinds = new Set(targets.map((target) => target.kind));

  if (targetKinds.has('claude-rule-pointer')) {
    writeClaudeRulePointers(context, targets.find((target) => target.kind === 'claude-rule-pointer'));
  } else {
    cleanupClaudeRulePointers(context);
  }

  if (targetKinds.has('opencode-instructions')) {
    writeOpenCodeRulesInstruction(context, targets.find((target) => target.kind === 'opencode-instructions'));
  } else {
    cleanupOpenCodeRulesInstruction(context, targets.find((target) => target.kind === 'opencode-instructions'));
  }
}

function writeClaudeRulePointers(context, target) {
  cleanupClaudeRulePointers(context);

  for (const ruleName of context.options.rules) {
    const targetPath = getClaudeRulePointerPath(context, target.root, ruleName);
    const content = renderClaudeRulePointer(context.options.harnessDir, ruleName);
    context.printAction(writeFile(targetPath, content, { dryRun: context.options.dryRun, overwrite: true }), targetPath);
  }
}

function cleanupClaudeRulePointers(context) {
  const rulesRoot = '.claude/rules';
  for (const ruleName of getAvailableRuleDirs(context.manifest.rulesRoot)) {
    const targetPath = getClaudeRulePointerPath(context, rulesRoot, ruleName);
    const action = removeFile(targetPath, { dryRun: context.options.dryRun });
    context.printAction(action, targetPath);
    if (action === 'remove') {
      removeEmptyDirsUntil(path.dirname(targetPath), safeResolveInside(context.workspaceDir, rulesRoot, 'claude rules root'), context.options.dryRun);
    }
  }
}

function getClaudeRulePointerPath(context, root, ruleName) {
  return safeResolveInside(context.workspaceDir, path.join(root, `niuma-${ruleName}.md`), 'claude rule pointer');
}

function renderClaudeRulePointer(harnessDir, ruleName) {
  return `# Niuma ${ruleName} Rules Pointer\n\nThis file is generated by \`niuma-harness\`.\n\nCanonical rule content lives under:\n\n- \`${harnessDir}/docs/rules/${ruleName}/\`\n\nLoad the relevant files in that directory when the current task needs these preferences. Do not duplicate rule content here.\n`;
}

function writeOpenCodeRulesInstruction(context, target) {
  if (context.options.rules.length === 0) {
    cleanupOpenCodeRulesInstruction(context, target);
    return;
  }

  const configPath = getOpenCodeConfigPath(context, target);
  const config = readOpenCodeConfig(configPath);
  const nextConfig = mergeOpenCodeRulesInstruction(config, context.options.rules, context.options.harnessDir);
  context.printAction(writeFile(configPath, `${JSON.stringify(nextConfig, null, 2)}\n`, { dryRun: context.options.dryRun, overwrite: true }), configPath);
}

function cleanupOpenCodeRulesInstruction(context, target) {
  const configPath = getOpenCodeConfigPath(context, target || { file: 'opencode.json' });
  if (!fs.existsSync(configPath)) {
    return;
  }

  const content = fs.readFileSync(configPath, 'utf8');
  if (!hasManagedBlock(content)) {
    return;
  }

  const config = readOpenCodeConfig(configPath);
  const nextConfig = removeOpenCodeRulesInstruction(config);
  context.printAction(writeFile(configPath, `${JSON.stringify(nextConfig, null, 2)}\n`, { dryRun: context.options.dryRun, overwrite: true }), configPath);
}

function getOpenCodeConfigPath(context, target) {
  return safeResolveInside(context.workspaceDir, target.file, 'opencode config');
}

function readOpenCodeConfig(configPath) {
  if (!fs.existsSync(configPath)) {
    return {};
  }

  const content = fs.readFileSync(configPath, 'utf8');
  let config;
  try {
    config = JSON.parse(content);
  } catch (error) {
    throw new Error(`Cannot update opencode.json because it is not valid JSON: ${error.message}`);
  }

  if (!config || Array.isArray(config) || typeof config !== 'object') {
    throw new Error('Cannot update opencode.json because it must contain a JSON object.');
  }

  return config;
}

function mergeOpenCodeRulesInstruction(config, rules, harnessDir) {
  if (rules.length === 0) {
    return removeOpenCodeRulesInstruction(config);
  }

  const nextConfig = { ...config };
  const block = renderOpenCodeRulesInstruction(rules, harnessDir);
  nextConfig.instructions = mergeManagedInstruction(nextConfig.instructions, block);
  return nextConfig;
}

function removeOpenCodeRulesInstruction(config) {
  const nextConfig = { ...config };
  if (!Object.prototype.hasOwnProperty.call(nextConfig, 'instructions')) {
    return nextConfig;
  }

  const cleaned = removeManagedInstruction(nextConfig.instructions);
  if (cleaned === undefined) {
    delete nextConfig.instructions;
  } else {
    nextConfig.instructions = cleaned;
  }
  return nextConfig;
}

function mergeManagedInstruction(instructions, block) {
  if (instructions === undefined) {
    return block;
  }

  if (typeof instructions === 'string') {
    return replaceManagedBlock(instructions, block);
  }

  if (Array.isArray(instructions)) {
    const next = [];
    let replaced = false;
    for (const instruction of instructions) {
      if (typeof instruction !== 'string') {
        throw new Error('Cannot update opencode.json because instructions must be a string or an array of strings.');
      }

      if (hasManagedBlock(instruction)) {
        if (!replaced) {
          next.push(block);
          replaced = true;
        }
        continue;
      }
      next.push(instruction);
    }

    if (!replaced) {
      next.push(block);
    }
    return next;
  }

  throw new Error('Cannot update opencode.json because instructions must be a string or an array of strings.');
}

function removeManagedInstruction(instructions) {
  if (typeof instructions === 'string') {
    const cleaned = removeManagedBlock(instructions).trim();
    return cleaned || undefined;
  }

  if (Array.isArray(instructions)) {
    const next = instructions.filter((instruction) => {
      if (typeof instruction !== 'string') {
        throw new Error('Cannot update opencode.json because instructions must be a string or an array of strings.');
      }
      return !hasManagedBlock(instruction);
    });
    return next.length > 0 ? next : undefined;
  }

  if (instructions === undefined) {
    return undefined;
  }

  throw new Error('Cannot update opencode.json because instructions must be a string or an array of strings.');
}

function renderOpenCodeRulesInstruction(rules, harnessDir) {
  return `${MANAGED_RULES_BEGIN}\nNiuma Harness rules live under \`${harnessDir}/docs/rules/\`.\nSelected rule directories: ${rules.join(', ')}.\nLoad relevant canonical rule files on demand; do not duplicate rule text in OpenCode config.\n${MANAGED_RULES_END}`;
}

function replaceManagedBlock(text, block) {
  if (!hasManagedBlock(text)) {
    return text ? `${text}\n\n${block}` : block;
  }

  return text.replace(new RegExp(`${escapeRegExp(MANAGED_RULES_BEGIN)}[\\s\\S]*?${escapeRegExp(MANAGED_RULES_END)}`), block);
}

function removeManagedBlock(text) {
  return text.replace(new RegExp(`\\n?${escapeRegExp(MANAGED_RULES_BEGIN)}[\\s\\S]*?${escapeRegExp(MANAGED_RULES_END)}\\n?`), '\n');
}

function hasManagedBlock(text) {
  return text.includes(MANAGED_RULES_BEGIN) && text.includes(MANAGED_RULES_END);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

module.exports = {
  MANAGED_RULES_BEGIN,
  MANAGED_RULES_END,
  writeRuleAdapterFiles,
};
